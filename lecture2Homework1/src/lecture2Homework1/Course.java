package lecture2Homework1;

public class Course {

	int courseID;
	String courseName;
	String courseDetail;
	int courseInstructorID;
	String courseInstructor;
	
	public Course(int courseID, String courseName, String courseDetail, int courseInstructorID,
			String courseInstructor) {
	
		this.courseID = courseID;
		this.courseName = courseName;
		this.courseDetail = courseDetail;
		this.courseInstructorID = courseInstructorID;
		this.courseInstructor = courseInstructor;
	}
	
}
